//Declara una variable número con un valor numérico y utiliza condicionales para 
//determinar en qué rango se encuentra.
let numero=20;

if(numero>=0 && numero<=10){
    console.log("El número se encuentra en el rango de 0 a 10.");

}else if(numero>=10 && numero<=20){
    console.log("El número se encuentra en el rango de 10 a 20.");
}else {
    console.log("El número no se encuentra en ningún rango.");
}